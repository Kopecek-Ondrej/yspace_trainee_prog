// my three functions
static void writePin(GPIO_TypeDef *GPIOx, uint16_t pin, uint8_t state)
{
	if(state)
	{
										// for both setting and resetting
		GPIOx -> BSRR = (uint32_t)pin;	// bit SET reset register, 32 bit register
										// first 16 bit set HIGH, second 16 bit set pin LOW
	}
	else
	{
										// BRR alternative way to reset pin
		GPIOx -> BRR = (uint32_t)pin; 	// bit RESET register, 32 bit register
	}
}
static uint8_t readPin(GPIO_TypeDef *GPIOx, uint16_t pin)
{
	uint8_t state;
									// each bit holds the state of a specific GPIO pin
	if ((GPIOx->IDR & pin) != 0)	// IDR(Input Data Register)
	{
		state = 1;
	}
	else
	{
		state = 0;
	}

	return state;

}
static void togglePin(GPIO_TypeDef *GPIOx, uint16_t pin)
{
							// ODR(output data register), each bit represents pin in a port
	GPIOx -> ODR ^= pin;	// xor inverts the value in odr
}
